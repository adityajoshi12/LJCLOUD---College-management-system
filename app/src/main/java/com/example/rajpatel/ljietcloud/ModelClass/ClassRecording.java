public class ClassRecording  {

    int id;
    String SUBJECT;
    String SUB_NOTES;
    String SUB_RECORDPATH;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getSUBJECT() {
        return SUBJECT;
    }

    public void setSUBJECT(String SUBJECT) {
        this.SUBJECT = SUBJECT;
    }

    public String getSUB_NOTES() {
        return SUB_NOTES;
    }

    public void setSUB_NOTES(String SUB_NOTES) {
        this.SUB_NOTES = SUB_NOTES;
    }

    public String getSUB_RECORDPATH() {
        return SUB_RECORDPATH;
    }

    public void setSUB_RECORDPATH(String SUB_RECORDPATH) {
        this.SUB_RECORDPATH = SUB_RECORDPATH;
    }
}
